    private void jButtonPodesiParametreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPodesiParametreActionPerformed
        // TODO add your handling code here:
        int odg = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da hocete da podesite parametre?");
        if (odg == 0) {
            try {
                String ipAdresa = jTextFieldIPAdresa.getText();
                int port = Integer.parseInt(jTextFieldPort.getText());

                file = new File("config.properties");
                configProperty.setProperty("ipAdresa", ipAdresa);
                configProperty.setProperty("port", Integer.toString(port));

                outputStream = new FileOutputStream(file);
                configProperty.store(outputStream, null);
                outputStream.close();
                JOptionPane.showMessageDialog(this, "Uspesno ste podesili parametre");
                dispose();
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "Port mora biti broj!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Nisu uspesno podeseni parametri!");
            }
        }
    }//GEN-LAST:event_jButtonPodesiParametreActionPerformed


