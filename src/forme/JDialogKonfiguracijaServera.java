    private void postaviConfig() throws FileNotFoundException, IOException {

        configProperty = new Properties();

        configProperty.setProperty("ipAdresa", "localhost");
        configProperty.setProperty("port", "9000");

        jTextFieldIPAdresa.setText(configProperty.getProperty("ipAdresa"));
        jTextFieldPort.setText(configProperty.getProperty("port"));
    }
