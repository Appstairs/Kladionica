    private void postaviPolja(double ukupnaUplata, double ukupnaIsplata, double potencijalnaIsplata) {
        jTextFieldUkupnaUplata.setText(Double.toString((Math.round((ukupnaUplata) * 100.0) / 100.0)));
        jTextFieldUkupnaIsplata.setText(Double.toString((Math.round((ukupnaIsplata) * 100.0) / 100.0)));
        jTextFieldPotencijalnaIsplata.setText(Double.toString((Math.round((potencijalnaIsplata) * 100.0) / 100.0)));
        jTextFieldStanje.setText(Double.toString((Math.round((ukupnaUplata - ukupnaIsplata) * 100.0) / 100.0)));
        jTextFieldPotencijalnoStanje.setText(Double.toString((Math.round((ukupnaUplata - ukupnaIsplata - potencijalnaIsplata) * 100.0) / 100.0)));
    }

    private void prikaziFormuPrijava() {
        JOptionPane.showMessageDialog(this, "Izgubili ste konekciju sa serverom! Probajte ponovo da se ulogujete!");
        FPrijava fp = new FPrijava();
        fp.setLocationRelativeTo(null);
        fp.setResizable(false);
        fp.setVisible(true);
    }

