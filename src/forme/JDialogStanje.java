    private void jButtonStanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStanjeActionPerformed
        try {
            // TODO add your handling code here:
            if (jDateChooserPrviDatum.getDate() == null || jDateChooserDrugiDatum.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Morate izabrati oba datuma");
                return;
            }

            double ukupnaUplata = 0;
            double ukupnaIsplata = 0;
            double potencijalnaIsplata = 0;

            HashMap<String, Date> hmap = new HashMap<>();
            hmap.put("prviDatum", (vratiDatumSaNulom(jDateChooserPrviDatum.getDate())));
            hmap.put("drugiDatum", (JPanelKreirajTiket.vratiDatumKraj(jDateChooserDrugiDatum.getDate())));

            KlijentTransferObjekat zahtev12 = new KlijentTransferObjekat();
            zahtev12.setOperacija(KlijentTransferObjekat.VRATI_TIKETE);
            zahtev12.setObjekatOperacije(hmap);
            KontrolerKI.vratiInstancu().posaljiZahtev(zahtev12);
            ServerTransferObjekat odgovor12 = KontrolerKI.vratiInstancu().procitajOdgovor();
            if (odgovor12.getSignal() == -1) {
                throw new Exception(odgovor12.getPoruka());
            }
            List<Tiket> tiketi = (List<Tiket>) odgovor12.getObjekatIzvrsenjaOperacije();

            for (Tiket tiket : tiketi) {
                if (tiket.getVremeUplate() != null) {
                    ukupnaUplata += tiket.getUplata();
                    if (tiket.getStatus().equals("isplacen")) {
                        ukupnaIsplata += tiket.getDobitak();
                    }
                    if (tiket.getStatus().equals("aktivan")) {
                        potencijalnaIsplata += tiket.getDobitak();
                    }
                }
            }

            postaviPolja(ukupnaUplata, ukupnaIsplata, potencijalnaIsplata);
            JOptionPane.showMessageDialog(this, odgovor12.getPoruka());
        } catch (IOException ex) {
            this.dispose();
            prikaziFormuPrijava();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButtonStanjeActionPerformed


